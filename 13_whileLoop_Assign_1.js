let i=5; let j=""; 
while(i<=15){
    j=`${j.concat(i)} `;
    i++;
}
console.log(`1. nos. from 5 to 15: ${j}.`);
console.log("");

let m=50, n="";
while(m>=40){
    n=`${n.concat(m)} `;
    m--
}
console.log(`2. nos. from 50 to 40: ${n}.`);
console.log("");

let a=1, b="",c=15;
while(c>0){
    b=`${b.concat(a)} `;
    a=a+2;
    c--;
}
console.log(`3. First 15 odd nos.: ${b}.`);
console.log("");

let d=0, e="", f=10;
